const mongoose = require("mongoose");

const fooditemSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    }
}, { timestamps: true })

const foodItem = mongoose.model("food_list", fooditemSchema);

module.exports = foodItem;